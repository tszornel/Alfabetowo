using System.Collections;
using System.Collections.Generic;
using UnityEngine;
public class TickBehaviour : StateMachineBehaviour
{
    public AudioClip sourceClip;
    private AudioSource audio;
    // OnStateEnter is called when a transition starts and the state machine starts to evaluate this state
    override public void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        GameLog.LogMessage("Thick behaviour on State entered");
        if (!audio)
        {
            audio = animator.gameObject.GetComponent<AudioSource>();
        }
        if (audio && sourceClip && sourceClip.length!=0)
            audio?.PlayOneShot(sourceClip);
    }
